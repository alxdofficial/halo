#!/usr/bin/env python3
"""Build the global label-to-index mapping for ConSE-tier baselines.

The ConSE baselines (crosshar, limubert, ssl_wearables) each train a single
classifier whose OUTPUT vocabulary is the sorted union of every training
dataset's activity labels. That vocabulary lives in
    benchmark_data/processed/limubert/global_label_mapping.json
and is consumed by grouped_zero_shot.map_local_to_global_labels (which indexes
into it by each dataset's sorted activity list) and by run_baselines_v2 (which
asserts a cached classifier emits exactly len(labels) classes).

This file used to be hand-maintained and drifted out of sync with
dataset_config.json (it lagged the capture24 train-set addition and the
dsads lying_side -> lying_right_side label correction, causing KeyErrors in
map_local_to_global_labels). Regenerate it deterministically from the config
whenever train_datasets or any train dataset's `activities` change:

    python benchmark_data/scripts/build_global_label_mapping.py

IMPORTANT: changing the vocabulary changes label indices, so every cached
ConSE head (crosshar_zs_transformer.pt, limubert_zs_gru.pt,
ssl_wearables_zs_head.pt) MUST be re-fit afterwards -- their old class count /
index alignment no longer matches. run_baselines_v2 guards this with an
explicit shape check.
"""

import json
from pathlib import Path

BENCHMARK_DIR = Path(__file__).resolve().parent.parent
CONFIG_PATH = BENCHMARK_DIR / "dataset_config.json"
OUT_PATH = BENCHMARK_DIR / "processed" / "limubert" / "global_label_mapping.json"


def build_labels(config: dict) -> list[str]:
    """Sorted union of every train dataset's activity labels."""
    train = config["train_datasets"]
    union: set[str] = set()
    for ds in train:
        union.update(config["datasets"][ds]["activities"])
    return sorted(union)


def main() -> None:
    with open(CONFIG_PATH) as f:
        config = json.load(f)
    train = config["train_datasets"]
    labels = build_labels(config)

    payload = {
        "description": (
            f"Global label-to-index mapping: sorted union of activity labels "
            f"across all {len(train)} training datasets ({', '.join(train)}). "
            f"Output vocabulary for the ConSE-tier baseline classifiers "
            f"(crosshar, limubert, ssl_wearables). Generated by "
            f"benchmark_data/scripts/build_global_label_mapping.py -- do not edit by hand."
        ),
        "labels": labels,
        "num_labels": len(labels),
    }

    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(OUT_PATH, "w") as f:
        json.dump(payload, f, indent=2)
    print(f"Wrote {OUT_PATH}")
    print(f"  {len(train)} train datasets -> {len(labels)} global labels")
    print(f"  first 5: {labels[:5]}")
    print(f"  last 5:  {labels[-5:]}")


if __name__ == "__main__":
    main()
